alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']
new_text = []

def cypher(direction, text, shift):
    
    for i in text:
        if i == " ":
            current = " "
            new_text.append(" ")
        
        elif i not in alphabet:
            new_text.append(i)

        else:
            current = alphabet.index(i)

            if direction == "encode":
                newindex = (current + shift)

            elif direction == "decode":
                newindex = (current - shift)

            new_text.append(alphabet[newindex % 26])
    
    print("Your next text is:")
    print(''.join(new_text))
    
    for i in range(len(new_text)):
        new_text.pop()


